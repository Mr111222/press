Object.keys(elComponents).forEach(key => {
  elComponents[key].install = function(Vue, options = {}) {
    if (elComponents[key].install.installed) return
    Vue.component(key, elComponents[key])
  }
})

function install (Vue, options = {}) {
  let components = {...elComponents, ...cbComponents}
  Object.keys(components).forEach(key => {
    Vue.use(components[key])
  })

  Vue.use(CbLoading.directive);
  Vue.prototype.$ELEMENT = {
    size: '',
    zIndex: 2000
  };

  Vue.prototype.$loading = CbLoading.service;
  Vue.prototype.$msgbox = CbMessageBox;
  Vue.prototype.$alert = CbMessageBox.alert;
  Vue.prototype.$confirm = CbMessageBox.confirm;
  Vue.prototype.$prompt = CbMessageBox.prompt;
  Vue.prototype.$notify = CbNotification;
  Vue.prototype.$message = CbMessage;

}

